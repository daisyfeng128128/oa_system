package com.baofeng.oa.bean;

import java.io.Serializable;

/**
 * 功能：子公司管理
 * */
public class BranchOfficeBean implements Serializable {

	private static final long serialVersionUID = 1L;

	private String id;
	/** 公司名称 */
	private String name;
	/** 公司简称 */
	private String ipgp;
	/** 员工前缀 */
	private String numberHead;
	/** 联系电话 */
	private String telPhone;
	/** 公司地址 */
	private String address;
	/** 电子邮箱 */
	private String email;
	/** 是否登陆 */
	private String islogin;
	/** 社保基数 */
	private String socialsecurity;
	/** 分公司id */
	private Integer branchs;
	
	
	
	

	public Integer getBranchs() {
		return branchs;
	}

	public void setBranchs(Integer branchs) {
		this.branchs = branchs;
	}

	public String getSocialsecurity() {
		return socialsecurity;
	}

	public void setSocialsecurity(String socialsecurity) {
		this.socialsecurity = socialsecurity;
	}

	public String getIpgp() {
		return ipgp;
	}

	public void setIpgp(String ipgp) {
		this.ipgp = ipgp;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNumberHead() {
		return numberHead;
	}

	public void setNumberHead(String numberHead) {
		this.numberHead = numberHead;
	}

	public String getTelPhone() {
		return telPhone;
	}

	public void setTelPhone(String telPhone) {
		this.telPhone = telPhone;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getIslogin() {
		return islogin;
	}

	public void setIslogin(String islogin) {
		this.islogin = islogin;
	}

}
